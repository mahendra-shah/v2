import React, { useState, useEffect, useContext } from "react";
import "./Form.css";
import { useLocation, useNavigate } from "react-router-dom";
import { LoginContext } from "../Context/LoginContext";
import DeleteIcon from "@mui/icons-material/Delete";
import EditIcon from "@mui/icons-material/Edit";
import SaveIcon from "@mui/icons-material/Save";
import LoadingSpinner from "../Loader/LoadingSpinner";
import { useLoader } from "../Context/LoadingContext";
import {
  Dialog,
  DialogActions,
  DialogContent,
  DialogContentText,
  DialogTitle,
  Button,
  Snackbar,
  Alert,
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  Paper,
  IconButton,
  TextField,
  Box,
} from "@mui/material";
import MuiAlert from "@mui/material/Alert";
import { DatePicker } from "@mui/x-date-pickers/DatePicker";
import { LocalizationProvider } from "@mui/x-date-pickers/LocalizationProvider";
import { AdapterDayjs } from "@mui/x-date-pickers/AdapterDayjs";
import dayjs from "dayjs";
import { extractEmailFromGoogleToken } from "../../utils/verifyGoogleToken";

const Form = () => {
  // Contexts
  const dataContext = useContext(LoginContext);
  const googleTokenPayload = extractEmailFromGoogleToken(localStorage.getItem("jwtToken"));
  const { email } = googleTokenPayload;
  
  const { loading, setLoading } = useLoader();

  // User details from localStorage
  const userName = googleTokenPayload.name ? googleTokenPayload.name : localStorage.getItem("name");
  const userDepartment = localStorage.getItem("department");

  // Get todayâ€™s date in YYYY-MM-DD format
  const getTodayDate = () => {
    const today = new Date();
    const yyyy = today.getFullYear();
    const mm = String(today.getMonth() + 1).padStart(2, "0");
    const dd = String(today.getDate()).padStart(2, "0");
    return `${yyyy}-${mm}-${dd}`;
  };

  // Initial state for form data
  const initialFormData = {
    email: email,
    selectedDate: dataContext.selectedDate || getTodayDate(),
    contributions: [],
    blockers: "",
  };

  // State hooks
  const [formData, setFormData] = useState(initialFormData);
  const [projectByDepartment, setProjectByDepartment] = useState({});
  const [filteredProjects, setFilteredProjects] = useState([]);
  const [projectsLoading, setProjectsLoading] = useState(true);
  const [selectedProject, setSelectedProject] = useState("");
  const [currentContribution, setCurrentContribution] = useState({
    hours: "0",
    task: "",
  });
  const [maxHours, setMaxHours] = useState(12);
  const [showProjectForm, setShowProjectForm] = useState(false);
  const [error, setError] = useState("");
  const [successMessage, setSuccessMessage] = useState("");
  const [saved, setSaved] = useState(false);
  const [showSelect, setShowSelect] = useState(true);
  const [editIndex, setEditIndex] = useState(null);
  const [editContribution, setEditContribution] = useState({
    hours: "",
    task: "",
  });

  // Delete confirmation dialog
  const [open, setOpen] = useState(false);
  const [deleteIndex, setDeleteIndex] = useState(null);

  // Attempts tracking
  const [previousEntriesDone, setPreviousEntriesDone] = useState(0);
  const [attempt, setAttempt] = useState(0);
  const [backdatedAttemptsLeft, setBackdatedAttemptsLeft] = useState(0);
  const [attemptLoading, setAttemptLoading] = useState(true);

  // Snackbar states
  const [snackbarOpen, setSnackbarOpen] = useState(false);
  const [snackbaropen, setSnackbaropen] = useState(false);
  const [snackbarMessage, setSnackbarMessage] = useState("");
  const [snackbarSeverity, setSnackbarSeverity] = useState("success");
  const [alertMessage, setAlertMessage] = useState("");

  // Error flags
  const [showProjectError, setShowProjectError] = useState(false);
  const [showHoursError, setShowHoursError] = useState(false);
  const [showTaskError, setShowTaskError] = useState(false);
  const [showSaveError, setShowSaveError] = useState(false);

  // Department data
  const [departments, setDepartments] = useState([]);
  const [projectNameToId, setProjectNameToId] = useState({});
  const [projectIdToName, setProjectIdToName] = useState({});

  const navigate = useNavigate();
  const location = useLocation();
  const today = new Date().toISOString().split("T")[0];

  // Base API URL
  const API_BASE_URL = import.meta.env.VITE_API_BASE_URL;

  // Snackbar Handlers 
  const handleCloseSnackbar = () => {
    setSnackbaropen(false);
  };

  const showSnackbar = (message, severity = "success") => {
    setSnackbarMessage(message);
    setSnackbarSeverity(severity);
    setSnackbarOpen(true);
  };

  const handleSnackbarClose = (event, reason) => {
    if (reason === "clickaway") return;
    setSnackbarOpen(false);
  };

  // Handle alert messages passed via navigation state
  useEffect(() => {
    if (location.state?.message) {
      setAlertMessage(location.state.message);
      setSnackbaropen(true)
      navigate(location.pathname, { replace: true });
    }
  }, [location.state, location.pathname, navigate]);

  // Fetch employees and project data
  useEffect(() => {
    let email = localStorage.getItem("email") ?? "";
    setAttemptLoading(true);

    // Reset monthly attempts if new month
    const initPreviousEntries = () => {
      const storedData = JSON.parse(localStorage.getItem("previousEntriesDone"));
      const today = new Date();
      const firstDayOfMonth = new Date(today.getFullYear(), today.getMonth(), 1);

      if (storedData) {
        if (new Date(storedData.lastUpdated).getTime() < firstDayOfMonth.getTime()) {
          localStorage.setItem(
            "previousEntriesDone",
            JSON.stringify({ count: 0, lastUpdated: today })
          );
          setPreviousEntriesDone(0);
        } else {
          setPreviousEntriesDone(storedData.count);
        }
      } else {
        localStorage.setItem(
          "previousEntriesDone",
          JSON.stringify({ count: 0, lastUpdated: today })
        );
        setPreviousEntriesDone(0);
      }
    };

    initPreviousEntries();

    // Fetch active projects
    try {
      fetch(`${API_BASE_URL}/employees`)
        .then((response) => response.json())
        .then((data) => {
          const projectByDepartment = {};
          const nameToIdMap = {};
          const idToNameMap = {};
          const projects = data.data.map((project) => {
            return {
              projectId: project.Id,
              projectName: project.projectName,
              status: project.projectStatus,
              department: project.department,
            };
          });

          const activeProjectNames = projects
            .map((project) => project.status === "Active" && project.projectName)
            .filter(Boolean);

          const today = new Date();
          const dayOfWeek = today.getDay();
          if (dayOfWeek === 6) {
            activeProjectNames.push("Saturday-Peer-Learning");
          }

          // Map projects by department
          projects.forEach((project) => {
            if (project.status === "Active") {
              const dept = project.department.trim();
              const projectName = project.projectName;

              // Department-based mapping
              if (!projectByDepartment[dept]) {
                projectByDepartment[dept] = new Set();
              }
              projectByDepartment[dept].add(projectName);

              // Name/ID mappings
              nameToIdMap[projectName] = project.projectId;
              idToNameMap[project.projectId] = projectName;
            }
          });

          // Convert Sets to Arrays
          Object.keys(projectByDepartment).forEach((dept) => {
            projectByDepartment[dept] = Array.from(projectByDepartment[dept]);
          });

          setProjectByDepartment(projectByDepartment);
          setProjectNameToId(nameToIdMap);
          setProjectIdToName(idToNameMap);
          setProjectsLoading(false);
        });
    } catch (error) {
      console.error("Error fetching data:", error);
    }
  }, []);

  // Department Filter
  useEffect(() => {
    const currentDept = formData.department || userDepartment;
    if (!currentDept) {
      setFilteredProjects([]);
      return;
    }

    // Fetch department-specific projects
    const fetchDepartmentProjects = async () => {
      try {
        const response = await fetch(`${API_BASE_URL}/employees?department=${encodeURIComponent(currentDept)}`);
        const data = await response.json();
        if (data?.data?.length > 0) {
          const activeProjects = data.data.filter(
            (project) => project.projectStatus === "Active"
          );
          let filtered = activeProjects.map((p) => ({ id: p.Id, name: p.projectName }));
          setFilteredProjects(filtered);
        } else {
          setFilteredProjects([]);
        }
      } catch (error) {
        console.error("Error fetching department projects:", error);
        setFilteredProjects([]);
      }
    };
    fetchDepartmentProjects();
  }, [formData.department, userDepartment]);

  // Set max hours
  useEffect(() => {
    setMaxHours(15);
  }, [selectedProject]);

  // Set save error
  useEffect(() => {
    if (showSaveError) {
      const timer = setTimeout(() => {
        setShowSaveError(false);
      }, 3000);

      return () => clearTimeout(timer);
    }
  }, [showSaveError]);

  // Fetch backdated attempts left for user
  useEffect(() => {
    const email = localStorage.getItem("email") ?? "";

    const fetchUserAttempts = async () => {
      try {
        // const response = await fetch(`${API_BASE_URL}/attempts`);
        const response = await fetch(`${API_BASE_URL}/attempts`, {
          method: "GET",
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${localStorage.getItem("jwtToken")}`,
          },
        });
        const data = await response.json();
        if (data?.data) {
          setBackdatedAttemptsLeft(data?.data?.attemptsLeft);
        }
      } catch (error) {
        console.error("Error fetching user attempts:", error);
      }
    };

    if (email) {
      fetchUserAttempts();
    }
  }, []);

  // Fetch list of all departments
  useEffect(() => {
    const fetchDepartments = async () => {
      try {
        const res = await fetch(
          `${API_BASE_URL}/employeeSheetRecords?sheet=pncdata`
        );
        const data = await res.json();
        if (data.success) {
          const allDepartments = data.data.map((item) => item.Department);
          const uniqueDepartments = [...new Set(allDepartments)];
          setDepartments(uniqueDepartments);
        }
      } catch (err) {
        console.error("Error fetching departments:", err);
      }
    };
    fetchDepartments();
  }, []);

  document.querySelectorAll('input[type="number"]').forEach(function (input) {
    input.addEventListener("wheel", function (event) {
      event.preventDefault();
    });
  });

  // Input & Form Handlers
  function handleChange(e) {
    const { name, value } = e.target;
    setFormData((prev) => ({
      ...prev,
      [name]: value,
    }));
  }

  // Project dropdown selection
  const handleProjectSelect = (e) => {
    setSaved(false);
    const selectedId = e.target.value;
    if (projectIdToName[selectedId] === "Ad-hoc tasks")
      setError("You can only log a maximum of 2 hours for Ad-hoc tasks");
    setSelectedProject(selectedId);
    setCurrentContribution({ hours: "", task: "" });
    if (e.target.value !== "") {
      setShowProjectError(false);
    }
  };

  // Check max hours validation
  function checkMaxValue(input) {
    if (projectIdToName[selectedProject] === "Ad-hoc tasks") {
      if (input.value > 2) input.value = 2;
      if (input.value < 0) input.value = 0;

    } else {
      if (input.value > 15) input.value = 15;
      if (input.value < 0) input.value = 0;
    }
  }

  // Change contribution handler
  const handleContributionChange = (e) => {
    const { name, value } = e.target;
    setCurrentContribution({
      ...currentContribution,
      [name]: value,
    });
  };

  // Add new contribution to list
  const addContribution = () => {
    if (!selectedProject) {
      setShowProjectError(true);
      return;
    } else {
      setShowProjectError(false);
    }

    if (!currentContribution.hours) {
      setShowHoursError(true);
      return;
    } else {
      setShowHoursError(false);
    }

    if (!currentContribution.task) {
      setShowTaskError(true);
      return;
    } else {
      setShowTaskError(false);
    }

    if (currentContribution.hours < 0) {
      setShowHoursError(true);
      return;
    } else {
      setShowHoursError(false);
    }

    setFormData((prevState) => ({
      ...prevState,
      contributions: [
        ...prevState.contributions,
        { projectId: selectedProject, project: projectIdToName[selectedProject], ...currentContribution, department: prevState.department || userDepartment },
      ],
      blockers: formData.blockers,
    }));

    setSaved(true);
    setSelectedProject("");
    setCurrentContribution({ hours: "", task: "" });
    setShowProjectForm(false);
  };

  // Edit contribution handlers
  const handleEditContributionChange = (e) => {
    const { name, value } = e.target;
    let newValue = value;

    if (name === "hours") {
      const project = formData.contributions[editIndex]?.project;

      if (project === "Ad-hoc tasks") {
        if (newValue > 2) newValue = 2;
        if (newValue < 0) newValue = 0;
      } else {
        if (newValue > 15) newValue = 15;
        if (newValue < 0) newValue = 0;
      }
    }
    setEditContribution({
      ...editContribution,
      [name]: newValue,
    });
  };

  // Edit handler
  const handleEdit = (index) => {
    setEditIndex(index);
    setEditContribution(formData.contributions[index]);
  };

  // Save edited contribution
  const handleSaveEdit = (index) => {
    const updatedContributions = formData.contributions.map(
      (contribution, idx) => (idx === index ? editContribution : contribution)
    );
    setFormData({
      ...formData,
      contributions: updatedContributions,
    });
    setEditIndex(null);
  };

  // Delete contribution
  const handleDelete = (index) => {
    setDeleteIndex(index);
    setOpen(true);
  };

  // Confirm delete
  const confirmDelete = () => {
    const updatedContributions = formData.contributions.filter(
      (contribution, idx) => idx !== deleteIndex
    );
    setFormData({
      ...formData,
      contributions: updatedContributions,
    });
    setOpen(false);
  };

  // Close delete confirmation dialog
  const handleClose = () => {
    setOpen(false);
  };

  // Form submission handler
  const handleSubmit = async (e) => {
    // Stop default form submit
    e.preventDefault();

    // Show loader
    setLoading(true);

    // Ensure contribution saved
    if (!saved) {
      setShowSaveError(true);
      return;
    }

    const userEmail = email;
    const department = localStorage.getItem("department");

    // Build payload for API
    const newEntry = {
      entries: formData.contributions.map((c) => ({
        email: userEmail,
        projectName: c.project,
        projectId: c.projectId,
        totalHoursSpent: Number(c.hours),
        workDescription: c.task,
        entryDate: formData.selectedDate,
        department: userDepartment,
        workingDepartment: c.department || userDepartment,
        ...(department === "Residential Program" && {
          blockers: formData.blockers,
        }),
      })),
    };

    // Save to localStorage for dashboard view
    const newLog = {
      date: newEntry.entryDate,
      project: newEntry.projectName,
      hours: newEntry.totalHoursSpent,
      description: newEntry.workDescription,
    };
    const existingLogs = JSON.parse(localStorage.getItem("dailyLogs")) || [];
    localStorage.setItem(
      "dailyLogs",
      JSON.stringify([...existingLogs, newLog])
    );

    // Validate contributions before submitting
    if (formData.contributions.length === 0) {
      setShowProjectError(true);
      return;
    }

    // Send data to backend API
    try {
      const response = await fetch(
        `${API_BASE_URL}/activityLogs`,
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
          },
          body: JSON.stringify(newEntry),
        }
      );

      const result = await response.json();

      if (!response.ok) {
        throw new Error(result.message || "Failed to save entry");
      }

      // Show success message
      showSnackbar("Entry successfully saved!", "success");

      // Case: No attempts left for backdated entries
      if (result.message === 'You already finished your 3 attempts') {
        setSnackbarMessage('You have 0 attempts remaining for backdated entries!');
        setSnackbarSeverity('warning');
        setSnackbarOpen(true);
        setFormData((prev) => ({ ...prev, contributions: [] }));
        setLoading(false);
        return;
      }

      // Get first result status
      const entryStatus = result?.results?.[0]?.status;
      const resultItem = result?.results?.[0];
      const EntryStatus = resultItem?.status;

      // Case: Entry explicitly failed
      if (EntryStatus === "failed") {
        showSnackbar(resultItem?.reason || "Entry was skipped or not saved.", "error");
        setFormData((prev) => ({ ...prev, contributions: [] }));
        setLoading(false);
        return;
      }

      // Case: Entry is not success or update
      if (entryStatus !== "success" && entryStatus !== "updated") {
        throw new Error(
          result?.results?.[0]?.message || "Entry was skipped or not saved."
        );
      }

      // Show remaining attempts for backdated entries if available
      if (result?.backdatedLeft?.[userEmail] !== undefined) {
        const backdatedLeft = result.backdatedLeft[userEmail];
        showSnackbar(
          `Entry successfully processed! Backdated entries left: ${backdatedLeft}`,
          "info"
        );
        setAttempt(backdatedLeft);
        localStorage.setItem("attemptsLeft", backdatedLeft);
      }

      // Case: Entry updated instead of newly created
      if (entryStatus === "updated") {
        showSnackbar("Entry successfully updated!", "info");
      }

      // Clear form after successful submission
      setFormData({ ...initialFormData });
      setLoading(false);

    } catch (error) {
      console.error("Error posting entry:", error);
      showSnackbar(error.message || "Failed to save entry", "error");
    }

    // Reset project error after submission
    setShowProjectError(false);
  };

  // Change page opacity while loading
  const handleLoading = (load) => {
    load == true
      ? (document.getElementById("root").style.opacity = "0.8")
      : (document.getElementById("root").style.opacity = "1");
  };

  // Get earliest valid date (last 3 working days, skip Sun + 2nd/4th Sat)
  function getMinDate() {
    const today = new Date();
    const validDates = [];

    // Start from yesterday
    let i = 1;
    while (validDates.length < 3) {
      const date = new Date(today);
      date.setDate(today.getDate() - i);

      // 0 = Sunday, 6 = Saturday
      const day = date.getDay();

      // Skip Sundays
      if (day === 0) {
        i++;
        continue;
      }

      // Skip 2nd and 4th Saturdays
      if (day === 6) {
        const dateNum = date.getDate();
        const month = date.getMonth();
        const year = date.getFullYear();
        let saturdayCount = 0;

        for (let d = 1; d <= dateNum; d++) {
          const tempDate = new Date(year, month, d);
          if (tempDate.getDay() === 6) {
            saturdayCount++;
          }
        }

        if (saturdayCount === 2 || saturdayCount === 4) {
          i++;
          continue;
        }
      }

      // Save valid date
      validDates.push(new Date(date));
      i++;
    }

    // Return the earliest valid date among the 3
    return validDates[validDates.length - 1].toISOString().split("T")[0];
  }

  // Current department: selected or default user department
  const currentDept = formData.department || userDepartment;

  return (
    <div className="form-container" style={{ overflowY: "scroll", height: "100vh", marginTop: "-20px" }}>

      {/* Loader */}
      <LoadingSpinner loading={loading} className="loader-container" />

      {/* Greeting Section */}
      <div style={{ textAlign: "center", marginTop: "1.5rem" }}>
        <h3 style={{ fontSize: "32px", fontWeight: "bold", color: "#000" }}>
          Welcome Back, <span style={{ color: "#2E7D32" }}>{userName}</span>
        </h3>
        <h3
          style={{
            fontSize: "1rem",
            fontWeight: "500",
            color: "#000",
            marginTop: "0.5rem",
          }}
        >
          Remaining Attempts for Backdated Entries : {" "}
          <span style={{ color: "#2E7D32", fontWeight: "bold" }}>
            {backdatedAttemptsLeft}
          </span>
        </h3>
        <h1
          style={{
            fontSize: "18px",
            fontWeight: "500",
            color: "#000",
            marginTop: "0.5rem",
          }}
        >
          Daily Employee's Activity Tracker
        </h1>
      </div>

      {/* Main Form */}
      <form onSubmit={handleSubmit} className="from-1">
        {successMessage && <h1 style={{ color: "green" }}>{successMessage}</h1>}

        {/* Employee Info */}
        <div>
          <label>Employee Email:</label>
          <input
            type="email"
            name="email"
            value={formData.email}
            onChange={handleChange}
            required
            disabled
            color="red"
          />
        </div>
        <div>
          <label>Employee Name:</label>
          <input
            type="text"
            name="name"
            value={userName}
            onChange={handleChange}
            required
            disabled
            color="red"
          />
        </div>
        <div>
          <label>Employee Department:</label>
          <input
            type="text"
            value={userDepartment}
            required
            disabled
            color="red"
          />
        </div>

        {/* Current Department Select */}
        <div>
          <label>Current Working Department:</label>
          <select
            name="department"
            value={formData.department || userDepartment}
            onChange={(e) => {
              const newDepartment = e.target.value;
              setFormData((prev) => ({
                ...prev,
                department: newDepartment,
              }));
              setSelectedProject("");
              setCurrentContribution({ hours: "", task: "" });
            }}
          >
            <option value="" disabled>
              Select Department
            </option>
            {departments.map((dept, idx) => (
              <option key={idx} value={dept}>
                {dept}
              </option>
            ))}
          </select>
        </div>

        {/* Date Picker */}
        <div>
          <label>Select the date for which you want to update the form:</label>
          <LocalizationProvider dateAdapter={AdapterDayjs}>
            <DatePicker
              value={dayjs(formData.selectedDate)}
              onChange={(newValue) => {
                setFormData((prev) => ({
                  ...prev,
                  selectedDate: newValue ? dayjs(newValue).format("YYYY-MM-DD") : ""
                }));
              }}
              format="DD/MM/YYYY"
              slotProps={{
                textField: {
                  fullWidth: true,
                  required: true,
                  size: "small",
                }
              }}
              minDate={dayjs(getMinDate())}
              maxDate={dayjs(today)}
            />
          </LocalizationProvider>
        </div>

        {/* Contributions Summary */}
        {formData.contributions.length > 0 && (
          <div>
            <h3>Contributions Summary</h3>
            <TableContainer
              component={Paper}
              sx={{
                width: '100%',
                maxWidth: '100%',
                overflow: 'hidden',
                boxShadow: 'none',
                border: '1px solid #ddd',
                margin: 0,
                padding: 0
              }}
            >
              <Table
                sx={{
                  width: '100%',
                  tableLayout: 'fixed',
                  margin: 0,
                  padding: 0,
                  '& .MuiTableCell-root': {
                    border: '1px solid #ddd',
                    padding: { xs: '6px 4px', sm: '8px 6px' },
                    overflow: 'hidden'
                  }
                }}
                size="small"
              >
                <TableHead sx={{ margin: 0, padding: 0 }}>
                  <TableRow sx={{ backgroundColor: '#f5f5f5', margin: 0, padding: 0 }}>

                    {/* Project */}
                    <TableCell
                      sx={{
                        fontWeight: 'bold',
                        width: { xs: '22%', sm: '18%' },
                        fontSize: { xs: '12px', sm: '14px' }
                      }}
                    >
                      Project
                    </TableCell>

                    {/* Hours */}
                    <TableCell
                      sx={{
                        fontWeight: 'bold',
                        width: { xs: '12%', sm: '10%' },
                        textAlign: 'center',
                        fontSize: { xs: '12px', sm: '14px' }
                      }}
                    >
                      Hours
                    </TableCell>

                    {/* Task */}
                    <TableCell
                      sx={{
                        fontWeight: 'bold',
                        width: { xs: '48%', sm: '55%' },
                        fontSize: { xs: '12px', sm: '14px' }
                      }}
                    >
                      Task
                    </TableCell>

                    {/* Action */}
                    <TableCell
                      sx={{
                        fontWeight: 'bold',
                        width: { xs: '18%', sm: '17%' },
                        textAlign: 'center',
                        fontSize: { xs: '12px', sm: '14px' }
                      }}
                    >
                      Action
                    </TableCell>
                  </TableRow>
                </TableHead>
                <TableBody>
                  {formData.contributions.map((contribution, index) => (
                    <TableRow key={index}>
                      <TableCell
                        sx={{
                          fontSize: { xs: '11px', sm: '13px' },
                          wordBreak: 'break-word',
                          overflow: 'hidden',
                          textOverflow: 'ellipsis'
                        }}
                      >
                        {contribution.project}
                      </TableCell>
                      <TableCell sx={{ textAlign: 'center' }}>
                        {editIndex === index ? (
                          <TextField
                            type="number"
                            name="hours"
                            value={editContribution.hours}
                            onChange={handleEditContributionChange}
                            inputPropes={{
                              min: 0,
                              max: formData.contributions[editIndex]?.project === "Ad-hoc tasks" ? 2 : 15,
                            }}
                            size="small"
                            sx={{
                              width: '50px',
                              '& .MuiInputBase-input': {
                                textAlign: 'center',
                                fontSize: { xs: '11px', sm: '13px' },
                                padding: '4px'
                              }
                            }}
                          />
                        ) : (
                          <span style={{ fontSize: window.innerWidth < 600 ? '11px' : '13px' }}>
                            {contribution.hours}
                          </span>
                        )}
                      </TableCell>
                      <TableCell>
                        {editIndex === index ? (
                          <TextField
                            name="task"
                            value={editContribution.task}
                            onChange={handleEditContributionChange}
                            multiline
                            minRows={2}
                            maxRows={4}
                            size="small"
                            sx={{
                              width: '100%',
                              '& .MuiInputBase-input': {
                                fontSize: { xs: '11px', sm: '13px' },
                                padding: '4px'
                              }
                            }}
                          />
                        ) : (
                          <Box
                            sx={{
                              minHeight: '80px',
                              maxHeight: '120px',
                              overflowY: 'auto',
                              overflowX: 'hidden',
                              wordBreak: 'break-word',
                              fontSize: { xs: '11px', sm: '13px' },
                              lineHeight: 1.4,
                              padding: '8px 4px',
                              display: 'flex',
                              alignItems: 'flex-start'
                            }}
                          >
                            {contribution.task}
                          </Box>
                        )}
                      </TableCell>
                      <TableCell sx={{ textAlign: 'center' }}>
                        <Box
                          sx={{
                            display: 'flex',
                            gap: '2px',
                            alignItems: 'center',
                            justifyContent: 'center'
                          }}
                        >
                          {editIndex === index ? (
                            <>
                              <IconButton
                                onClick={() => handleSaveEdit(index)}
                                size="small"
                                sx={{
                                  color: '#666',
                                  padding: '2px',
                                  minWidth: 'auto',
                                  width: '24px',
                                  height: '24px',
                                  '&:hover': {
                                    color: '#4CAF50'
                                  }
                                }}
                              >
                                <SaveIcon sx={{ fontSize: '20px' }} />
                              </IconButton>
                              <IconButton
                                onClick={() => handleDelete(index)}
                                size="small"
                                sx={{
                                  color: '#666',
                                  padding: '2px',
                                  minWidth: 'auto',
                                  width: '25px',
                                  height: '25px',
                                  '&:hover': {
                                    color: '#f44336'
                                  }
                                }}
                              >
                                <DeleteIcon sx={{ fontSize: '20px' }} />
                              </IconButton>
                            </>
                          ) : (
                            <>
                              <IconButton
                                onClick={() => handleEdit(index)}
                                size="small"
                                sx={{
                                  color: '#666',
                                  padding: '2px',
                                  minWidth: 'auto',
                                  width: '25px',
                                  height: '25px',
                                  '&:hover': {
                                    color: '#4CAF50'
                                  }
                                }}
                              >
                                <EditIcon sx={{ fontSize: '20px' }} />
                              </IconButton>
                              <IconButton
                                onClick={() => handleDelete(index)}
                                size="small"
                                sx={{
                                  color: '#666',
                                  padding: '2px',
                                  minWidth: 'auto',
                                  width: '25px',
                                  height: '25px',
                                  '&:hover': {
                                    color: '#f44336'
                                  }
                                }}
                              >
                                <DeleteIcon sx={{ fontSize: '16px' }} />
                              </IconButton>
                            </>
                          )}
                        </Box>
                      </TableCell>
                    </TableRow>
                  ))}
                </TableBody>
              </Table>
            </TableContainer>
            <br />
            <p style={{ color: "green" }}>
              You can select multiple projects by clicking on the dropdown below
            </p>
          </div>
        )}

        {/* Project Selection */}
        <div>
          <label>Select a project in which you contributed:</label>
          {projectsLoading ? (
            <select disabled>
              <option value="">Loading projects...</option>
            </select>
          ) : Object.keys(projectByDepartment).length > 0 && currentDept in projectByDepartment ? (
            <select
              name="selectedProject"
              value={selectedProject}
              onChange={handleProjectSelect}
            >
              <option value="">--Select a project--</option>
              {filteredProjects.length > 0 ? (
                filteredProjects.map((p, index) => (
                  <option key={index} value={p.id}>
                    {p.name}
                  </option>
                ))
              ) : (
                <option disabled>No projects available</option>
              )}
            </select>
          ) : (
            <select disabled>
              <option value="">No projects available for your department</option>
            </select>
          )}
          {showProjectError && (
            <div style={{ display: "flex", color: "red", marginTop: "4px", fontSize: "0.85rem" }}>
              Project cannot be empty*
            </div>
          )}
          <br />
          <br />

          {/* Hours and Task Input */}
          {selectedProject && (
            <div>
              <label>Total Hours Spent:</label>
              <input
                type="number"
                name="hours"
                max={maxHours}
                value={currentContribution.hours}
                onChange={handleContributionChange}
                onInput={(e) => checkMaxValue(e.target)}
                min="0"
              />
              {showHoursError && (
                <div style={{ display: "flex", color: "red", marginTop: "4px", fontSize: "0.85rem" }}>
                  Total hours spent cannot be empty or negative*
                </div>
              )}
              <br />
              <label>What did you achieve in this project?</label>
              <textarea
                name="task"
                value={currentContribution.task}
                onChange={handleContributionChange}
              />
              {showTaskError && (
                <div style={{ display: "flex", color: "red", marginTop: "4px", fontSize: "0.85rem" }}>
                  Task cannot be empty*
                </div>
              )}
              <button
                type="button"
                onClick={addContribution}
                className="full-width-button"
              >
                Save Contribution
              </button>
            </div>
          )}
        </div>

        {/* Save Error */}
        {showSaveError && (
          <p style={{ display: "flex", color: "red", marginTop: "4px" }}>
            Please save your contribution before submitting.
          </p>
        )}

        {/* Submit Button */}
        <button type="submit" className="full-width-button" disabled={formData.contributions.length === 0 || editIndex !== null}>
          Submit
        </button>
      </form>

      {/* Delete Confirmation Dialog */}
      <Dialog
        open={open}
        onClose={handleClose}
        aria-labelledby="alert-dialog-title"
        aria-describedby="alert-dialog-description"
      >
        <DialogTitle id="alert-dialog-title">{"Confirm Delete"}</DialogTitle>
        <DialogContent>
          <DialogContentText id="alert-dialog-description">
            Are you sure you want to delete this contribution?
          </DialogContentText>
        </DialogContent>
        <DialogActions>
          <Button onClick={confirmDelete} color="primary" autoFocus>
            Yes
          </Button>
          <Button onClick={handleClose} color="primary">
            No
          </Button>
        </DialogActions>
      </Dialog>

      {/* Snackbar for notifications */}
      <Snackbar
        open={snackbarOpen}
        autoHideDuration={4000}
        onClose={handleSnackbarClose}
        anchorOrigin={{ vertical: "bottom", horizontal: "center" }}
      >
        <Alert
          onClose={handleSnackbarClose}
          severity={snackbarSeverity}
          sx={{ width: "100%" }}
        >
          {snackbarMessage}
        </Alert>
      </Snackbar>

      <Snackbar
        open={snackbaropen}
        autoHideDuration={6000}
        onClose={handleCloseSnackbar}
        anchorOrigin={{ vertical: "bottom", horizontal: "left" }}
      >
        <MuiAlert
          elevation={6}
          variant="filled"
          onClose={handleCloseSnackbar}
          severity={snackbarSeverity}
        >
          {alertMessage}
        </MuiAlert>
      </Snackbar>
    </div>
  );
};

export default Form;
